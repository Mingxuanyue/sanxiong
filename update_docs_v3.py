# ─── 段落文字替换（保留第一个 Run 的字体格式） ────────────────────────────
def replace_para(p, old, new):
    full = p.text
    if old not in full:
        return False
    replaced = full.replace(old, new)
    # 清空所有 run，把新文本写进第一个 run（保留格式）
    if p.runs:
        fmt = p.runs[0]  # 借用第一个 run 的格式
        for r in p.runs:
            r.text = ''
        fmt.text = replaced
    else:
        p.add_run(replaced)
    return True

def replace_all(doc, old, new):
    count = 0
    for p in doc.paragraphs:
        if replace_para(p, old, new):
            count += 1
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for p in cell.paragraphs:
                    if replace_para(p, old, new):
                        count += 1
    return count

