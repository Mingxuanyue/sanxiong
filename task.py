def tbl(doc, headers, rows):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = 'Table Grid'
    for i, h in enumerate(headers):
        t.rows[0].cells[i].text = h
    for r in rows:
        row = t.add_row()
        for i, c in enumerate(r):
            row.cells[i].text = c
    doc.add_paragraph(u"")
    return t

